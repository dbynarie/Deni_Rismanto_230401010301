const showModal = () => {
    let modal = document.querySelector('.modalMenu')
    modal.style.display = 'flex'

}


const hideModal = () => {

    let modal = document.querySelector('.modalMenu')
    modal.style.display = 'none'
}

const hideAlert = () => {
    let aboutAlert = document.querySelector('.aboutAlert')
    aboutAlert.style.display = 'none'
}